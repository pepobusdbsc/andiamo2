// Connect to the Binance Smart Chain using web3.js
async function connectWeb3() {
  if (window.BinanceChain) {
    await window.BinanceChain.enable();
    window.web3 = new Web3(window.BinanceChain);
    return true;
  }
  return false;
}

// Lock the specified amount of tokens for the given time period
async function lockTokens() {
  const token = document.getElementById('token').value;
  const amount = document.getElementById('amount').value;
  const time = document.getElementById('time').value;
  const beneficiary = document.getElementById('beneficiary').value;

  if (!token || !amount || !time || !beneficiary) {
    alert('Please fill in all the required fields');
    return;
  }

  try {
    const web3Connected = await connectWeb3();
    if (!web3Connected) {
      alert('Binance Smart Chain connection failed');
      return;
    }

    const contract = new web3.eth.Contract(contractABI, contractAddress);
    const lockTime = Math.floor(new Date(time).getTime() / 1000); // Convert time to Unix timestamp

    // Convert token amount to the required format
    const tokenAmount = web3.utils.toWei(amount, 'ether');

    await contract.methods.lockTokens(token, tokenAmount, lockTime, beneficiary).send({ from: web3.eth.defaultAccount });

    alert('Tokens locked successfully');
  } catch (error) {
    console.error(error);
    alert('An error occurred while locking tokens');
  }
}

